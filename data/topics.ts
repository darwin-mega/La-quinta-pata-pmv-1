import { DebateTopic, TOPIC_CATEGORY_OPTIONS, TopicCategoryId, TopicIntensity } from "@/lib/topic-types";

export type LegacyTopicIntensity = "liviano" | "medio" | "filoso";

type TopicSeed = {
    id: string;
    legacyIntensity: LegacyTopicIntensity;
    category: TopicCategoryId;
    statement: string;
    angleA: string;
    angleB: string;
};

const CATEGORY_LABELS = new Map(
    TOPIC_CATEGORY_OPTIONS.map(category => [category.id, category.label])
);

const normalizeIntensity = (topicId: string, legacyIntensity: LegacyTopicIntensity): TopicIntensity => {
    if (legacyIntensity === "liviano") return "baja";
    if (legacyIntensity === "medio") return "media";

    const numericPart = Number.parseInt(topicId.replace(/^\D+/, ""), 10);
    return numericPart > 10 ? "muy_alta" : "alta";
};

const buildContext = (category: TopicCategoryId, statement: string) => {
    const label = CATEGORY_LABELS.get(category) || category;
    return `Tema de ${label}. La sala debate una tesis clara y debe sostenerla o refutarla con ejemplos, criterios y consecuencias reales. Tesis: "${statement}"`;
};

const buildPrompts = () => [
    "Que ejemplo concreto hace mas fuerte tu postura?",
    "Que costo o limite ves en la postura contraria?",
    "Que criterio usarias para convencer a alguien que no piensa como tu equipo?",
];

const toDebateTopic = (seed: TopicSeed): DebateTopic => ({
    id: seed.id,
    text: seed.statement,
    statement: seed.statement,
    category: seed.category,
    categoryLabel: CATEGORY_LABELS.get(seed.category) || seed.category,
    intensity: normalizeIntensity(seed.id, seed.legacyIntensity),
    source: "system",
    context: buildContext(seed.category, seed.statement),
    angleA: seed.angleA,
    angleB: seed.angleB,
    prompts: buildPrompts(),
    enabled: true,
});

